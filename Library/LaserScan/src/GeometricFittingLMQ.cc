//Fits a bicubic function to the given data. The form of the function is
// a1*x^3 + a2*y^3 + a3*x^2*y + a4*x*y^2 + a5*x^2 + a6*y^2 + a7*x*y + a8*x + a9*y + a10*z + a11 = 0;
